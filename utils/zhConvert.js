const S2T_MAP = {};
for (const [t, s] of Object.entries(T2S_MAP)) {
    if (t.length === 1 && s.length === 1) S2T_MAP[s] = t;
}

const _t2sRe = new RegExp(`[${Object.keys(T2S_MAP).filter(k => k.length === 1).join('')}]`, 'g');

/**
 * 繁体 → 简体（轻量映射，用于搜索归一化）
 * @param {string} str
 * @returns {string}
 */
export function t2s(str) {
    if (!str) return str;
    return str.replace(_t2sRe, ch => T2S_MAP[ch] || ch);
}

/**
 * 检测文本是否包含繁体中文字符
 * @param {string} str
 * @returns {boolean}
 */
export function hasTraditional(str) {
    return _t2sRe.test(str);
}
